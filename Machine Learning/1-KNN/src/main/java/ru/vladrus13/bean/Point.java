package ru.vladrus13.bean;

/**
 * Point class
 */
public class Point {
    /**
     * X-axis position
     */
    public final int x;
    /**
     * Y-axis position
     */
    public final int y;

    /**
     * @param x x-axis position
     * @param y y-axis position
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
